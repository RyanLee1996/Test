package ryan.hibernate;

public class GoodsEntity {
    private int id;
    private String name;
    private CustomerEntity customerByUid;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        GoodsEntity that = (GoodsEntity) o;

        if (id != that.id) return false;
        if (name != null ? !name.equals(that.name) : that.name != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        return result;
    }

    public CustomerEntity getCustomerByUid() {
        return customerByUid;
    }

    public void setCustomerByUid(CustomerEntity customerByUid) {
        this.customerByUid = customerByUid;
    }
}
